-- Find total revenue generated by each customer 

SELECT c.customer_firstname,
	   c.customer_lastname,
	   agg.revenue
FROM (SELECT 
			  s.customer_key,
			   SUM(p.product_cost*s.sls_quantity) tot_cost,
			   SUM(s.sls_price) tot_sales,
			   (SUM(s.sls_price)-SUM(p.product_cost*s.sls_quantity)) revenue
		FROM gold.fact_sales s
		LEFT JOIN gold.dim_products p
		ON s.product_key = p.product_key
		GROUP BY s.customer_key) agg
LEFT JOIN gold.dim_customers c
ON agg.customer_key = c.customer_key;

